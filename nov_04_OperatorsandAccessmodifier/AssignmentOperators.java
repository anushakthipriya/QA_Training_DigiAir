package nov_04_OperatorsandAccessmodifier;

public class AssignmentOperators {

	public static int c, a=10, b=20;

	
        public void class1()
        {
        	System.out.println (c = a); 	
        }
        public static void main(String[] args) 
        {
        System.out.println (c = a); 
        System.out.println (b += a); 
        System.out.println (b -= a); 
        System.out.println (b *= a); 
        System.out.println (b /= a); 
        System.out.println (b %= a); 
        System.out.println (b ^= a); 
	}

}
