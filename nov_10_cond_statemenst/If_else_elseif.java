package nov_10_cond_statemenst;

public class If_else_elseif {

	public static void main(String[] args) {
		 int number = 4;
		    
		    if (number == 1) {
		      System.out.println("The number is one");
		    } else if (number == 2) {
		      System.out.println("The given number is two");
		    } else if (number == 3) {
		      System.out.println("The number is three!");
		    } else {
		      System.out.println("No condition satisfied so else block will get printed !!");
		    }

	}

}
