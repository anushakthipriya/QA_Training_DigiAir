package nov_10_cond_statemenst;

public class For {

	public static void main(String[] args) 
	{
		 int sum = 0;
		 int n = 1000;

		    for (int i = 1; i <= n; ++i) 
		    {
		      sum += i;     
		    }
		       
		    System.out.println("Sum of natural numbers from 1 to 1000 = " + sum);

	}

}
